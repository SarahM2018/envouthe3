$(document).keyup(e => {
  if (e.keyCode == 27) {
    $("body").css("background-color", "pink");
}
})

$(document).keyup(e => {
  if (e.keyCode == 38) {
$("body").css("background-color", "yellow");
}
})

$(document).keyup(e => {
  if (e.keyCode == 40) {
$("body").css("background-color", "blue");
}
})
